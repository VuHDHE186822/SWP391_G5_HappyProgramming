    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        User user = (User) session.getAttribute("user");

        // Lấy phần tệp từ yêu cầu
        Part filePart = request.getPart("avatarFile");

        try (InputStream fileContent = filePart.getInputStream()) {
            byte[] avatarBytes = fileContent.readAllBytes(); // Đọc tất cả byte từ InputStream

            // Chuyển đổi byte array thành chuỗi Base64
            String avatar = ImageConverter.gI().encode(avatarBytes);
            user.setAvatarPath(avatar);
            UserDAO dao = new UserDAO();
            dao.updateUserAvatarPath(user.getUsername(), user.getAvatarPath());
        }

        response.sendRedirect("userProfile.jsp");
    }

